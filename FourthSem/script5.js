let a = 5;
let b = 10;

console.log("AND operator");
console.log(a == 5 && b == 10);
console.log(a == 1 && b == 10);
console.log(a == 5 && b == 2);
console.log(a == 12 && b == 11);
console.log("OR operator");
console.log(a == 5 || b == 10);
console.log(a == 1 || b == 10);
console.log(a == 5 || b == 2);
console.log(a == 12 || b == 11);
console.log("NOT operator");
console.log(!(a == 5 === b == 10));
